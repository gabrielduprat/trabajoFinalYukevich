    private void btnParticipanteSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnParticipanteSiguienteActionPerformed
        String numeroParticipante = txtNumeroParticipante.getText();
        int numero = Integer.parseInt(numeroParticipante);
        if (numero < 10) {
            numero = numero + 1;
            numeroParticipante = String.valueOf(numero);
            txtNumeroParticipante.setText(numeroParticipante);
        }
        if(participantes[numero] != null){
            Pasajero pasajeros[] = participantes[numero].getPasajeros();
            txtNombreApellido.setText(pasajeros[numero].getNombreApellido());
            txtDni.setText(pasajeros[numero].getDni());
            txtFechaNacimiento.setText(pasajeros[numero].getFechaNacimiento());
            txtMatricula.setText(participantes[numero].getMatricula());
            Horario horarios[] = participantes[numero].getHorarios();
            txtHorarioPartida.setText(horarios[numero].getPartida());
            txtHorarioLlegada.setText(horarios[numero].getLlegada());            
        }else{
            txtNombreApellido.setText(null);
            txtDni.setText(null);
            txtFechaNacimiento.setText(null);
            txtMatricula.setText(null);
            txtHorarioPartida.setText("");
            txtHorarioLlegada.setText("");
            }
     
    }//GEN-LAST:event_btnParticipanteSiguienteActionPerformed

