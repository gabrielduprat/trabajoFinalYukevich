    private void btnGuardarParticipanteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarParticipanteActionPerformed
        String numeroParticipante = txtNumeroParticipante.getText();
        int numero = Integer.parseInt(numeroParticipante);
        String categoria = (String) comboCategoria.getSelectedItem();
        Participante participante = new Participante(categoria,txtMatricula.getText(),numero,pasajeros,horarios);
        participantes[numero] = participante;
        
        for (Pasajero pasajero : pasajeros) {
            pasajero = null;
        }
        for (Horario horario : horarios) {
            horario = null;
        }
    }//GEN-LAST:event_btnGuardarParticipanteActionPerformed

