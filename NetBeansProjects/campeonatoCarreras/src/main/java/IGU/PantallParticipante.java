    private void btnPasajeroSiguienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPasajeroSiguienteActionPerformed
        String numeroPasajero = txtNumeroPasajero.getText();
        int numero = Integer.parseInt(numeroPasajero);
        String categoria = (String) comboCategoria.getSelectedItem();

        if("Auto".equals(categoria) || "Camion".equals(categoria) ){
            if(numero < 3){
                numero = numero + 1;  
            }
        }
        if("Karting".equals(categoria)){
            if(numero < 1){
                numero = numero + 1;  
            }
        }
        numeroPasajero = String.valueOf(numero);
        txtNumeroPasajero.setText(numeroPasajero);
        // Datos pasajero
        //Pasajero pasajeroActual = pasajeros[numero];
        if (pasajeros[numero] != null){
            txtNombreApellido.setText(pasajeros[numero].getNombreApellido());
            txtDni.setText(pasajeros[numero].getDni());
            txtFechaNacimiento.setText(pasajeros[numero].getFechaNacimiento());
        }else{
            txtNombreApellido.setText(null);
            txtDni.setText(null);
            txtFechaNacimiento.setText(null);
        }
    }//GEN-LAST:event_btnPasajeroSiguienteActionPerformed

    private void btnPasajeroAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPasajeroAnteriorActionPerformed
        String numeroPasajero = txtNumeroPasajero.getText();
        int numero = Integer.parseInt(numeroPasajero);
        if (numero > 0) {
            numero = numero - 1;
            numeroPasajero = String.valueOf(numero);
            txtNumeroPasajero.setText(numeroPasajero);
            // Datos pasajero
            if (pasajeros[numero] != null){
                txtNombreApellido.setText(pasajeros[numero].getNombreApellido());
                txtDni.setText(pasajeros[numero].getDni());
                txtFechaNacimiento.setText(pasajeros[numero].getFechaNacimiento());
            }else{
                txtNombreApellido.setText(null);
                txtDni.setText(null);
                txtFechaNacimiento.setText(null);
            }            
        }
    }//GEN-LAST:event_btnPasajeroAnteriorActionPerformed

